namespace PDFiumZ.HighLevel;

/// <summary>
/// PDF 页面旋转角度
/// </summary>
public enum PdfPageRotation
{
    /// <summary>
    /// 0 度（默认）
    /// </summary>
    Rotate0 = 0,

    /// <summary>
    /// 90 度
    /// </summary>
    Rotate90 = 1,

    /// <summary>
    /// 180 度
    /// </summary>
    Rotate180 = 2,

    /// <summary>
    /// 270 度
    /// </summary>
    Rotate270 = 3
}
