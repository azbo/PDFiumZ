namespace PDFiumZ.HighLevel;

/// <summary>
/// Defines the position of a watermark on a PDF page.
/// </summary>
public enum WatermarkPosition
{
    /// <summary>
    /// Center of the page.
    /// </summary>
    Center,

    /// <summary>
    /// Top-left corner.
    /// </summary>
    TopLeft,

    /// <summary>
    /// Top-center.
    /// </summary>
    TopCenter,

    /// <summary>
    /// Top-right corner.
    /// </summary>
    TopRight,

    /// <summary>
    /// Middle-left.
    /// </summary>
    MiddleLeft,

    /// <summary>
    /// Middle-right.
    /// </summary>
    MiddleRight,

    /// <summary>
    /// Bottom-left corner.
    /// </summary>
    BottomLeft,

    /// <summary>
    /// Bottom-center.
    /// </summary>
    BottomCenter,

    /// <summary>
    /// Bottom-right corner.
    /// </summary>
    BottomRight
}
