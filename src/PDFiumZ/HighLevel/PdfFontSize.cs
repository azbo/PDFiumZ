namespace PDFiumZ.HighLevel;

/// <summary>
/// Provides commonly used font sizes for PDF documents.
/// All sizes are in points.
/// </summary>
public static class PdfFontSize
{
    /// <summary>Very small text (6pt)</summary>
    public const double VerySmall = 6;

    /// <summary>Small text (8pt)</summary>
    public const double Small = 8;

    /// <summary>Footnote text (9pt)</summary>
    public const double Footnote = 9;

    /// <summary>Normal/body text (10pt)</summary>
    public const double Normal = 10;

    /// <summary>Default body text (12pt)</summary>
    public const double Default = 12;

    /// <summary>Medium text (14pt)</summary>
    public const double Medium = 14;

    /// <summary>Large text (16pt)</summary>
    public const double Large = 16;

    /// <summary>Heading 4 (18pt)</summary>
    public const double Heading4 = 18;

    /// <summary>Heading 3 (20pt)</summary>
    public const double Heading3 = 20;

    /// <summary>Heading 2 (24pt)</summary>
    public const double Heading2 = 24;

    /// <summary>Heading 1 (28pt)</summary>
    public const double Heading1 = 28;

    /// <summary>Extra large text (32pt)</summary>
    public const double ExtraLarge = 32;

    /// <summary>Display text (36pt)</summary>
    public const double Display = 36;

    /// <summary>Title text (48pt)</summary>
    public const double Title = 48;

    /// <summary>Large title text (60pt)</summary>
    public const double LargeTitle = 60;

    /// <summary>Giant text (72pt)</summary>
    public const double Giant = 72;
}
